                                                            Michael Andrews was born in Norwich, England, the second child of Thomas Victor Andrews and his wife Gertrude Emma Green. He completed his National service between 1947 and 1949, nineteen months of which was spent in Egypt.[1] From 1949 to 1953 he studied at the Slade School of Fine Art under William Coldstream, Lucian Freud, William Townsend and Lawrence Gowing. Fellow students and friends there included Victor Willing, Keith Sutton, Diana Cumming, Euan Uglow and Craigie Aitchison. In 1953 he spent six months in Italy after receiving a Rome Scholarship in Painting.</p>

